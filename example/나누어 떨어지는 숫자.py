input_value = input("정수를 입력해주세요: ")
input_value = int(input_value)

if input_value % 2 == 0:
    print(f'{input_value}은 2로 나누어 떨어지는 숫자입니다.')
else:
    print(f'{input_value}은 2로 나누어 떨어지는 숫자가 아닙니다.')
if input_value % 3 == 0:
    print(f'{input_value}은 3로 나누어 떨어지는 숫자입니다.')
else:
    print(f'{input_value}은 3로 나누어 떨어지는 숫자가 아닙니다.')
if input_value % 4 == 0 :
    print(f'{input_value}은 4로 나누어 떨어지는 숫자입니다.')
else:
    print(f'{input_value}은 4로 나누어 떨어지는 숫자가 아닙니다.')
if input_value % 5 == 0:
    print(f'{input_value}은 5로 나누어 떨어지는 숫자입니다.')
else:
    print(f'{input_value}은 5로 나누어 떨어지는 숫자가 아닙니다.')